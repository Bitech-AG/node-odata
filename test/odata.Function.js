var PORT, conn, odata, request, should, support;

should = require('should');

request = require('supertest');

odata = require('../.');

support = require('./support');

PORT = 0;

conn = 'mongodb://localhost/odata-test';

describe('odata.Function', function() {
  return it('get should work', function(done) {
    var s, server, test;
    test = odata.Function();
    test.get('/test', function(req, res, next) {
      return res.jsonp({
        test: 'ok'
      });
    });
    server = odata(conn);
    server.use(test);
    return s = server.listen(PORT, function() {
      PORT = s.address().port;
      return request("http://localhost:" + PORT).get("/test").expect(200, done);
    });
  });
});

